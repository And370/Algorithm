# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def sumNumbers(self, root: TreeNode) -> int:
        if not root:
            return 0

        result = [0]

        def dfs(node, num):
            # å¶å­èŠ‚ç‚¹
            if not node.left and not node.right:
                result[0] += num * 10 + node.val
            else:
                # éå¶å­èŠ‚ç‚¹çš„å·¦å³å­æ ‘
                num = num * 10 + node.val
                if node.left:
                    dfs(node.left, num)
                if node.right:
                    dfs(node.right, num)

        dfs(root, 0)
        return result[0]

