class Solution:
    def jump(self, nums: List[int]) -> int:
        if len(nums) <= 1:
            return 0
        n = len(nums)

        max_value, step, boundary = 0, 0, 0

        for index in range(n - 1):
            # å½“å‰æœ€å¤§å€¼
            max_value = max(max_value, index + nums[index])
            # è‹¥é‡åˆ°å½“å‰è·³è·ƒè¾¹ç•Œ,åˆ™è¿›ä¸€æ­¥
            if index == boundary:
                boundary = max_value
                step += 1
        return step

